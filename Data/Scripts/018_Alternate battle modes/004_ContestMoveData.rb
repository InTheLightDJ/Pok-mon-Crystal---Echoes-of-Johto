  # Returns [type, appeal, jam] for a move, or [nil, 0, 0] if no data.
  def self.for(move_id)
    return MOVES[move_id] || [nil, 0, 0]
  end

  def self.type_for(move_id)
    return (MOVES[move_id] || [nil])[0]
  end

  def self.appeal_for(move_id)
    return (MOVES[move_id] || [nil, 0])[1]
  end

  def self.jam_for(move_id)
    return (MOVES[move_id] || [nil, 0, 0])[2]
  end

  def self.has_contest_data?(move_id)
    return MOVES.key?(move_id)
  end
